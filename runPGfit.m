disp('          ["rc1SCAN02.nii","rc2SCAN02.nii"],');
disp('          ["rc1SCAN03.nii","rc2SCAN03.nii"],');
disp('                  :                :        ');
disp('          ["rc1SCANXX.nii","rc2SCANXX.nii"]]');
disp(' ');

